package module.one.java;

public class Operators {
	public static void main(String[] args) {
		int a = 7;
		int b = 9;
		
		System.out.println("a + b = " +  (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b));
		System.out.println("a++ = " + (a++));
		System.out.println("b-- = " + (b--));
	}
}
